package Entidades;

public class Ensina {
private String aprende;
private String vagabundo;
private int animal;

public int getAnimal() {
	return animal;
}
public void setAnimal(int animal) {
	this.animal = animal;
}
public String getAprende() {
	return aprende;
}
public void setAprende(String aprende) {
	this.aprende = aprende;
}
public String getVagabundo() {
	return vagabundo;
}
public void setVagabundo(String vagabundo) {
	this.vagabundo = vagabundo;
}

}
